package com.lawer.model;

public class DouyuRoom {


    private String yxName;

    private String zbName;

    private String num;

    private String roomId;

    private String title;

    private String zbImage;

    private String name;

    public String getYxName() {
        return yxName;
    }

    public void setYxName(String yxName) {
        this.yxName = yxName;
    }

    public String getZbName() {
        return zbName;
    }

    public void setZbName(String zbName) {
        this.zbName = zbName;
    }

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }

    public String getRoomId() {
        return roomId;
    }

    public void setRoomId(String roomId) {
        this.roomId = roomId;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getZbImage() {
        return zbImage;
    }

    public void setZbImage(String zbImage) {
        this.zbImage = zbImage;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
